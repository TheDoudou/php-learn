            <h1>Le Crocodile</h1>
            <p>Les Crocodiles (Crocodylinae) constituent une sous-famille de crocodiliens de la famille des crocodilidés.</p>
            <p>Le mot vient du latin crocodilus, lui-même issu du grec ancien κροκόδειλος, krokodeilos, qui, selon Hérodote serait initialement le nom donné par les Ioniens aux lézards épineux des murailles et par analogie aurait été étendu aux crocodiles vivant dans le Nil. La forme actuelle, « crocodile », a longtemps coexisté avec le crocodelle et cocodrille avant d'être seule conservée au xvie siècle.</p>
