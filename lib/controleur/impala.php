            <h1>L'Impala</h1>
            <p>L'impala (Aepyceros melampus) est une espèce de mammifère faisant partie de l’ordre des artiodactyles et plus précisément de la famille des bovidés.
                On le retrouve principalement à l’est de l’Afrique centrale et du sud (notamment en Afrique du Sud, en Angola, en République démocratique du Congo, au Rwanda, en Ouganda et au Kenya).
                Deux sous-espèces sont reconnues. Cette espèce est la seule du genre Aepyceros.</p>
